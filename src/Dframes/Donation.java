    void add(){
        try{
            String FirstName = fnametf.getText();
            String LastName = lnametf.getText();
            int Age = Integer.parseInt(agetf.getText());
            String CardType = (String)ctypecb.getSelectedItem();
            int CardNumber = Integer.parseInt(cnotf.getText());
            String ExpiryDate = (String)month.getSelectedItem() + (String)year.getSelectedItem();
            Double Amount = Double.parseDouble(amttf.getText());
            
            String q = "Insert into DONATIONS values('" + FirstName + "','" + LastName + "'," + Age + ",'" + CardType +  "'," + CardNumber + ",'" + ExpiryDate + "'," + Amount + ")"; 
            
            Statement stmt = con.createStatement();
            stmt.executeUpdate(q);
            
            JOptionPane.showMessageDialog(this, "DONATION SUCCESSFUL! HAVE A NICE DAY");
            
            fnametf.setText("");
            lnametf.setText("");
            agetf.setText("");
            cnotf.setText("");
            amttf.setText("");
            
            
        }catch(Exception exp){
            JOptionPane.showMessageDialog(this, exp.getMessage());
        }
    }

